jQuery(document).ready(function($) {
	$("#header").find('.scroll-down').on('click', function ()
	{
		$('html, body').animate({
			scrollTop: $("#features").offset().top
		}, 300);
	});

	// Price plans switcher
	$('.price-plans-switcher a').on('click', function(e)
	{
		e.preventDefault();

		if ($(this).hasClass('active')) return;

		var target = $(this).attr('href');
		var speed = 400;
		var distance = 120;

		$('.price-plans-switcher').find('a').removeClass('active');
		$(this).addClass('active');

		$('.price-plans.active')
			.stop(true, false)
			.animate({
				left: distance,
				opacity: 0
			}, speed, function() {
				$(this)
					.removeClass('active')
					.css({
						left: 0
					});
			});

		$(target)
			.addClass('active')
			.css({
				left: -distance,
				opacity: 0
			})
			.stop(true, false)
			.animate({
				left: 0,
				opacity: 1
			}, speed);
	});
	
	// Keywords animation
	// based on https://codyhouse.co/gem/css-animated-headlines/
	animateKeyWords();

	function animateKeyWords()
	{
		//set animation timing
		var animationDelay = 2500;

		//initialise headline animation
		animateHeadline($('.keywords'));

		function animateHeadline($headlines) {
			$headlines.each(function(){
				var headline = $(this);

				setTimeout(function(){
					hideWord( headline.find('.is-visible').eq(0) )
				}, animationDelay);
			});
		}

		function hideWord($word) {
			var nextWord = takeNext($word);
			
			switchWord($word, nextWord);
			setTimeout(function(){ hideWord(nextWord) }, animationDelay);
		}

		function takeNext($word) {
			return (!$word.is(':last-child')) ? $word.next() : $word.parent().children().eq(0);
		}

		function takePrev($word) {
			return (!$word.is(':first-child')) ? $word.prev() : $word.parent().children().last();
		}

		function switchWord($oldWord, $newWord) {
			$oldWord.removeClass('is-visible').addClass('is-hidden');
			$newWord.removeClass('is-hidden').addClass('is-visible');
		}
	}
});
